<p style="margin-top:10px;display:inline-block;">
<b>JEFF HILL </b>, <i>Chief Executive Officer (CEO), First Physicians Capital Group</i><br /> 
Jeff Hill is the CEO of First Physicians Capital Group and joined the organization in March 2020. He is an innovative leader in the healthcare community with over 30 years of medical and clinical experience. His background in organizational, clinical and service excellence help bring a visionary perspective to his leadership expertise.
</p>
<br/>
<p>
Hill previously served in other executive roles including regional vice president of Quorum Health Resources in Brentwood, Tennessee and the CEO of both Steele Memorial Medical Center and Midwest Medical Center. He graduated from Wichita State University with a degree in medical technology and received his master’s degree in health administration from the University of Colorado-Denver.
</p>
<p>
Hill has been recognized in Becker’s Hospital Review as 50 Rural Hospital CEOs to Know in 2014 and 2015. He was awarded Press Ganey’s Guardian of Excellence for patient experience as well as the National Rural Health Association’s Top 20 Critical Access Hospital in America.
</p>
</div>
<hr class="style2" />
<p>
<b>Adrian Reeder</b>, <i>Executive Vice President (EVP) & Chief Financial Officer</i><br />  
Mr. Reeder has served as the Company’s Executive Vice President and Chief Financial Officer since April 10, 2014. Mr. Reeder brings over 14 years of experience in finance and accounting. 
</p>
<p>
Since 2013, Mr. Reeder served as a senior consultant for Now CFO, a professional services firm providing outsourced CFO services and provided advisory and consulting services to the Company prior to his appointment as the Company’s Chief Financial Officer. From 2010 until 2013, Mr. Reeder served as audit manager and consultant for Kezos &amp; Dunlavy, LLC, an accounting firm. Prior to 2010, Mr. Reeder was involved in several ventures, including founding Old Cobblestone Road, LLC, an online retail company, and serving as director of finance for Elim Valley Planning &amp; Development, LLC, a 2,400 acre master planned real estate development. Mr. Reeder began his career with KPMG, LLP and held the position of manager at his departure. Mr. Reeder received a B.S. and Masters in Accountancy in 2000 from Brigham Young University and is a Certified Public Accountant.
</p>



